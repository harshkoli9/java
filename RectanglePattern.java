public class RectanglePattern{
    public static void main(String[] args){
        
        int r = 4; //row
        int c = 5; //column
        
        for(int i = 1 ; i<=r ; i++){
            for(int j = 1 ; j<=c; j++){
                System.out.print("*");
                
            }
            System.out.println("");
        }
    }
}